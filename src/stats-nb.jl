function choose_numbers(dice; advantage = 0, disadvantage = 0)
	sorted_dice = sort(dice)
	if advantage > 0
		total = sum(sorted_dice[1+advantage:end])
	elseif disadvantage > 0
		total = sum(sorted_dice[1:end-disadvantage])
	else
		total = sum(sorted_dice)
	end
	total
end	

