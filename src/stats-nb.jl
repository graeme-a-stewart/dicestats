# ╔═╡ b5ad7e79-3083-46eb-950c-fc18afee6c60
function dice_stats(die, rolls; advantage=0, disadvantage=0)
	if advantage > 0 && disadvantage > 0
		throw(ArgumentError, "Cannot roll with advantage and disadvantage")
	end
	rolled_dice = rolls + advantage + disadvantage
	sum = 0
	total_rolls = die^rolled_dice
	dice = ones(Int, rolled_dice)
	for _ in 1:total_rolls
		pick = choose_numbers(dice; advantage, disadvantage)
		sum += pick
		#println("$dice $pick")
		dice[end] += 1
		for each_die in rolled_dice:-1:1
			if dice[each_die] > die
				if each_die == 1
					break
				end
				dice[each_die] = 1
				dice[each_die-1] += 1
			end
		end
	end
	sum / total_rolls
end
		

